DROP SEQUENCE IF EXISTS "author_id seq";
CREATE SEQUENCE "author_id seq" INCREMENT BY 50 START 1;


CREATE TABLE "authors" (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS SEQUENCE author_id_seq,
    name VARCHAR(255) NOT NULL,
    age INTEGER NOT NULL,
    description TEXT NOT NULL,
    image TEXT NOT NULL
);

CREATE TABLE author (
    id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS SEQUENCE author_id_seq,
    name VARCHAR(255) NOT NULL,
    age INTEGER NOT NULL,
    description TEXT NOT NULL,
    image TEXT NOT NULL
);